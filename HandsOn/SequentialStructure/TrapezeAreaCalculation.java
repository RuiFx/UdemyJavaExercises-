package SequentialStructure;

public class TrapezeAreaCalculation {
    public static void main(String[] args) {

        double b, h, B, area;
        b = 6.0;
        h = 5.0;
        B = 8.0;

        area = ((b + B) / 2) * h;

        System.out.println("The trapeze have 5 inches of height, base of 8 inches and top with 6 inches\n" + "So, the area of trapeze is " + area);
    }
}
